#!/usr/bin/env node
/**
 * content-quality-audit.js — Guide/content depth + honesty audit (local HTML).
 * Run: node scripts/content-quality-audit.js
 * Writes: CONTENT_QUALITY_AUDIT_<date>.md
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const TODAY = new Date().toISOString().slice(0, 10);
const OUT = path.join(ROOT, `CONTENT_QUALITY_AUDIT_${TODAY}.md`);

const TIER_A_MIN = 1200;
const TIER_B_MIN = 700;
const BOILER_RE = /hub:\s*<a href="\/category\/|compare<\/a> · <a href="\/plan-my-trip\/|Hand-checked · No paid placements/gi;

function stripMainBody(html) {
  const m = html.match(/<main[^>]*>([\s\S]*?)<\/main>/i);
  if (!m) return '';
  return m[1]
    .replace(/<script[\s\S]*?<\/script>/gi, '')
    .replace(/<style[\s\S]*?<\/style>/gi, '')
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function wordCount(text) {
  if (!text) return 0;
  return text.split(/\s+/).filter(Boolean).length;
}

function readTimeClaimed(html) {
  const m = html.match(/guide-byline-time[^>]*>([^<]+)/i);
  return m ? m[1].trim() : '';
}

function layoutType(html) {
  if (html.includes('cat-venue-grid') && html.includes('guide-byline-time')) return 'ranked';
  if (html.includes('guide-byline-time') && html.includes('venue-body')) return 'editorial';
  if (html.includes('venue-hero-meta')) return 'ranked-legacy';
  return 'other';
}

function hasGuideBodySource(slug) {
  const dir = path.join(ROOT, 'scripts', 'guide-bodies');
  if (!fs.existsSync(dir)) return false;
  const files = fs.readdirSync(dir).filter(f => f.endsWith('.js'));
  const norm = slug.replace(/-pattaya$/, '');
  return files.some(f => {
    const base = f.replace('.js', '');
    return slug.includes(base) || base.includes(norm) || norm.includes(base);
  });
}

function countInjectBlocks(html) {
  const m = html.match(/deepen-r\d+-block/g);
  return m ? m.length : 0;
}

function faqSchemaMismatch(html, slug) {
  const faqMatch = html.match(/<script type="application\/ld\+json">(\{[^<]*"@type":"FAQPage"[^<]*)<\/script>/);
  if (!faqMatch) return false;
  try {
    const j = JSON.parse(faqMatch[1]);
    const first = j.mainEntity && j.mainEntity[0] && j.mainEntity[0].name;
    if (!first) return false;
    const slugWords = slug.replace(/-/g, ' ').toLowerCase();
    const q = String(first).toLowerCase();
    if (q.includes('best muay thai') && !slug.includes('muay-thai')) return true;
    if (q.includes('compare these side-by-side') && !slug.includes('compare')) return true;
    return false;
  } catch {
    return false;
  }
}

function tierFor(words) {
  if (words >= TIER_A_MIN) return 'A';
  if (words >= TIER_B_MIN) return 'B';
  return 'C';
}

function listGuides() {
  const guidesDir = path.join(ROOT, 'guides');
  return fs.readdirSync(guidesDir, { withFileTypes: true })
    .filter(e => e.isDirectory() && fs.existsSync(path.join(guidesDir, e.name, 'index.html')))
    .map(e => e.name)
    .sort();
}

function main() {
  const slugs = listGuides();
  const rows = [];
  for (const slug of slugs) {
    const fp = path.join(ROOT, 'guides', slug, 'index.html');
    const html = fs.readFileSync(fp, 'utf8');
    const body = stripMainBody(html);
    const words = wordCount(body);
    const boilerHits = (body.match(BOILER_RE) || []).length;
    rows.push({
      slug,
      words,
      tier: tierFor(words),
      layout: layoutType(html),
      readTime: readTimeClaimed(html),
      injects: countInjectBlocks(html),
      hasSource: hasGuideBodySource(slug),
      weeklyMarquee: html.includes('UPDATED WEEKLY'),
      faqMismatch: faqSchemaMismatch(html, slug),
      boilerHits,
    });
  }
  rows.sort((a, b) => a.words - b.words);

  const tierC = rows.filter(r => r.tier === 'C');
  const tierB = rows.filter(r => r.tier === 'B');
  const tierA = rows.filter(r => r.tier === 'A');
  const issues = rows.filter(r => r.injects > 0 || r.faqMismatch || r.weeklyMarquee);

  const lines = [
    `# Content quality audit — ${TODAY}`,
    '',
    `Generated by \`node scripts/content-quality-audit.js\``,
    '',
    '## Thresholds',
    `- **Tier A** (keep): ≥ ${TIER_A_MIN} words in &lt;main&gt; body`,
    `- **Tier B** (deepen): ${TIER_B_MIN}–${TIER_A_MIN - 1} words`,
    `- **Tier C** (rewrite or de-emphasise): &lt; ${TIER_B_MIN} words`,
    '',
    '## Summary',
    `| Tier | Count |`,
    `|------|-------|`,
    `| A | ${tierA.length} |`,
    `| B | ${tierB.length} |`,
    `| C | ${tierC.length} |`,
    `| Guides total | ${rows.length} |`,
    '',
    `| Flags | Count |`,
    `|-------|-------|`,
    `| deepen-rNN inject blocks | ${rows.filter(r => r.injects > 0).length} |`,
    `| FAQ schema mismatch | ${rows.filter(r => r.faqMismatch).length} |`,
    `| UPDATED WEEKLY in HTML | ${rows.filter(r => r.weeklyMarquee).length} |`,
    '',
    '## Tier C — rewrite queue (worst first)',
    '',
  ];
  for (const r of tierC) {
    lines.push(`- **${r.words}w** \`/guides/${r.slug}/\` — ${r.layout}, read: ${r.readTime || 'n/a'}${r.injects ? `, injects: ${r.injects}` : ''}${r.faqMismatch ? ', ⚠ FAQ schema' : ''}`);
  }
  lines.push('', '## Tier B — deepen next', '');
  for (const r of tierB.slice(0, 20)) {
    lines.push(`- **${r.words}w** \`/guides/${r.slug}/\` — ${r.layout}${r.hasSource ? ', has guide-bodies/*.js' : ''}`);
  }
  if (tierB.length > 20) lines.push(`- … and ${tierB.length - 20} more`);
  lines.push('', '## Integrity flags (any tier)', '');
  for (const r of issues.filter(x => x.injects || x.faqMismatch || x.weeklyMarquee)) {
    const flags = [];
    if (r.injects) flags.push(`inject×${r.injects}`);
    if (r.faqMismatch) flags.push('FAQ mismatch');
    if (r.weeklyMarquee) flags.push('UPDATED WEEKLY');
    lines.push(`- \`/guides/${r.slug}/\` (${r.words}w) — ${flags.join(', ')}`);
  }
  lines.push('', '## Full table', '', '| Words | Tier | Layout | Read time | Slug |', '|------:|------|--------|-----------|------|');
  for (const r of rows) {
    lines.push(`| ${r.words} | ${r.tier} | ${r.layout} | ${r.readTime || '—'} | ${r.slug} |`);
  }
  lines.push('', '---', '*Re-run after content changes.*');

  fs.writeFileSync(OUT, lines.join('\n'), 'utf8');
  console.log(`Wrote ${OUT}`);
  console.log(`Tier A: ${tierA.length} · B: ${tierB.length} · C: ${tierC.length}`);
  if (tierC.length) {
    console.log(`Tier C worst: ${tierC[0].slug} (${tierC[0].words}w)`);
  }
  process.exit(tierC.length > 0 ? 0 : 0);
}

main();
