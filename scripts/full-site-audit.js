#!/usr/bin/env node
/**
 * full-site-audit.js — Local integrity + live production smoke test.
 * Run: node scripts/full-site-audit.js
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');
const crypto = require('crypto');

const ROOT = path.resolve(__dirname, '..');
const SITE = 'https://pattaya-gym.com';
const TODAY = new Date().toISOString().slice(0, 10);
const OUT = path.join(ROOT, `FULL_AUDIT_${TODAY}.md`);
const CONCURRENCY = 12;
const FETCH_TIMEOUT_MS = 15000;

const SISTER_SITES = [
  { name: 'Pattaya Visa Help', url: 'https://pattayavisahelp.com/' },
  { name: 'Pattaya Medical', url: 'https://pattaya-medical.com/' },
  { name: 'Pattaya School Guide', url: 'https://pattaya-school-guide.com/' },
  { name: 'Pattaya Vehicle Rentals', url: 'https://pattaya-vehicle-rentals.com/' },
  { name: 'TIMPAEMI', url: 'https://timpaemi.com/' },
  { name: 'TIMPAEMI Live', url: 'https://timpaemi.com/live/' },
];

const KEY_PAGES = [
  '/',
  '/guides/',
  '/search/',
  '/compare/',
  '/plan-my-trip/',
  '/sports/',
  '/category/muay-thai/',
  '/category/clubs/',
  '/category/fitness/',
  '/guides/best-muay-thai-pattaya/',
  '/guides/pattaya-russian-speaking-sport/',
  '/guides/snooker-pool-billiards-pattaya/',
  '/guides/equestrian-pattaya/',
  '/gyms/wko-muay-thai/',
  '/gyms/rusich-club-football/',
  '/gyms/sf-strike-bowl/',
  '/status.json',
  '/sitemap.xml',
  '/robots.txt',
  '/changelog/',
];

function readSitemapUrls() {
  const xml = fs.readFileSync(path.join(ROOT, 'sitemap.xml'), 'utf8');
  return [...xml.matchAll(/<loc>(https:\/\/pattaya-gym\.com[^<]+)<\/loc>/g)].map(m => m[1]);
}

async function fetchUrl(url) {
  const ctrl = new AbortController();
  const timer = setTimeout(() => ctrl.abort(), FETCH_TIMEOUT_MS);
  try {
    const res = await fetch(url, {
      signal: ctrl.signal,
      headers: { 'User-Agent': 'PattayaGym-Audit/1.0', Accept: 'text/html,application/json,*/*' },
      redirect: 'follow',
    });
    const body = await res.text();
    return { url, status: res.status, ok: res.ok, body, finalUrl: res.url };
  } catch (e) {
    return { url, status: 0, ok: false, error: e.message, body: '' };
  } finally {
    clearTimeout(timer);
  }
}

async function mapPool(items, fn, limit) {
  const out = [];
  let i = 0;
  async function worker() {
    while (i < items.length) {
      const idx = i++;
      out[idx] = await fn(items[idx], idx);
    }
  }
  await Promise.all(Array.from({ length: Math.min(limit, items.length) }, worker));
  return out;
}

function extractTitle(html) {
  const m = html.match(/<title[^>]*>([^<]+)<\/title>/i);
  return m ? m[1].trim() : '';
}

function extractCanonical(html) {
  const m = html.match(/<link[^>]+rel=["']canonical["'][^>]+href=["']([^"']+)["']/i);
  return m ? m[1] : '';
}

function checkHtmlHealth(url, body) {
  const issues = [];
  if (!body) return ['empty body'];
  const isNonHtml = url.endsWith('.json') || url.endsWith('.xml') || url.endsWith('/robots.txt');
  if (body.includes('\0')) issues.push('NUL bytes');
  if (!isNonHtml && !body.trimEnd().endsWith('</html>')) {
    issues.push('missing </html>');
  }
  if (/\$\{[^}]+\}/.test(body.replace(/<script[\s\S]*?<\/script>/gi, ''))) issues.push('unresolved template literal');
  if (/undefined/.test(body.slice(0, 5000))) issues.push('literal "undefined" in head');
  const skipTitle = url.endsWith('.json') || url.endsWith('.xml') || url.endsWith('/robots.txt');
  if (!extractTitle(body) && !skipTitle) issues.push('missing <title>');
  return issues;
}

function runLocal(cmd, label) {
  try {
    execSync(cmd, { cwd: ROOT, stdio: 'pipe', encoding: 'utf8' });
    return { label, ok: true };
  } catch (e) {
    return { label, ok: false, output: (e.stdout || '') + (e.stderr || '') };
  }
}

function localAssetVersion() {
  const src = fs.readFileSync(path.join(ROOT, 'build-v2.js'), 'utf8');
  const m = src.match(/ASSET_VERSION\s*=\s*['"](\d+)['"]/);
  return m ? m[1] : '?';
}

function countGuides() {
  const d = path.join(ROOT, 'guides');
  return fs.readdirSync(d, { withFileTypes: true })
    .filter(e => e.isDirectory() && fs.existsSync(path.join(d, e.name, 'index.html'))).length;
}

async function main() {
  console.log('Full site audit starting…');
  const lines = [];
  const push = (s = '') => { lines.push(s); console.log(s); };

  push(`# Pattaya.Gym full audit — ${TODAY}`);
  push('');
  push(`Generated by \`node scripts/full-site-audit.js\` at ${new Date().toISOString()}`);
  push('');

  // --- Local gates ---
  push('## 1. Local integrity gates');
  const localChecks = [
    runLocal('node scripts/verify-deploy.js', 'verify-deploy.js'),
    runLocal('node scripts/verify.js', 'verify.js'),
    runLocal('npm run validate', 'npm run validate'),
    runLocal('npm run html:validate', 'html:validate'),
  ];
  for (const c of localChecks) {
    push(`- **${c.label}**: ${c.ok ? 'PASS' : 'FAIL'}`);
    if (!c.ok) push('  ```\n' + (c.output || '').trim().slice(0, 2000) + '\n  ```');
  }
  push('');

  const localMeta = {
    assetVersion: localAssetVersion(),
    guides: countGuides(),
    sitemapUrls: readSitemapUrls().length,
  };
  push('## 2. Local repo snapshot');
  push(`- ASSET_VERSION: **v${localMeta.assetVersion}**`);
  push(`- Editorial guides on disk: **${localMeta.guides}**`);
  push(`- Sitemap URLs: **${localMeta.sitemapUrls}**`);
  push('');

  // --- Live key pages ---
  push('## 3. Live production — key pages');
  const keyResults = await mapPool(
    KEY_PAGES.map(p => SITE + p),
    async url => {
      const r = await fetchUrl(url);
      r.issues = checkHtmlHealth(url, r.body);
      r.title = extractTitle(r.body);
      r.canonical = extractCanonical(r.body);
      return r;
    },
    CONCURRENCY
  );

  let keyFails = 0;
  for (const r of keyResults) {
    const pathOnly = r.url.replace(SITE, '');
    const bad = !r.ok || r.issues.length;
    if (bad) keyFails++;
    push(`- \`${pathOnly}\` — HTTP **${r.status || 'ERR'}**${r.title ? ` · "${r.title.slice(0, 60)}"` : ''}${r.issues.length ? ` · ⚠ ${r.issues.join(', ')}` : ''}`);
    if (r.error) push(`  - fetch error: ${r.error}`);
  }
  push('');

  // --- status.json ---
  push('## 4. Live status.json vs local');
  const statusRes = keyResults.find(r => r.url.endsWith('/status.json'));
  if (statusRes && statusRes.ok) {
    try {
      const live = JSON.parse(statusRes.body);
      push(`- Live version: **v${live.version || live.assetVersion || '?'}**`);
      push(`- Live URLs: **${live.urls || live.sitemapUrls || '?'}**`);
      push(`- Live guides: **${live.guides || '?'}**`);
      push(`- Built: ${live.built || live.buildTimestamp || 'n/a'}`);
      if (String(live.version || live.assetVersion) !== localMeta.assetVersion) {
        push(`- ⚠ **Version drift** — local v${localMeta.assetVersion}, live v${live.version || live.assetVersion} (Cloudflare may still propagating)`);
      } else {
        push('- ✓ Version matches local repo');
      }
    } catch (e) {
      push(`- ⚠ Could not parse status.json: ${e.message}`);
    }
  } else {
    push('- ⚠ status.json unreachable');
  }
  push('');

  // --- Full sitemap crawl ---
  push('## 5. Full sitemap crawl (all URLs)');
  const sitemapUrls = readSitemapUrls();
  push(`Crawling **${sitemapUrls.length}** URLs from sitemap…`);
  const crawlResults = await mapPool(sitemapUrls, fetchUrl, CONCURRENCY);

  const byStatus = {};
  const broken = [];
  const unhealthy = [];
  for (const r of crawlResults) {
    byStatus[r.status] = (byStatus[r.status] || 0) + 1;
    if (!r.ok) broken.push(r);
    else {
      const issues = checkHtmlHealth(r.url, r.body);
      if (issues.length) unhealthy.push({ ...r, issues });
    }
  }
  push('');
  push('Status code distribution:');
  for (const [code, n] of Object.entries(byStatus).sort()) push(`- **${code}**: ${n}`);
  push('');
  push(`- Broken (non-2xx): **${broken.length}**`);
  push(`- 200 but HTML issues: **${unhealthy.length}**`);
  if (broken.length) {
    push('');
    push('### Broken URLs');
    for (const r of broken.slice(0, 40)) {
      push(`- ${r.status || 'ERR'} ${r.url.replace(SITE, '')}${r.error ? ` (${r.error})` : ''}`);
    }
    if (broken.length > 40) push(`- … and ${broken.length - 40} more`);
  }
  if (unhealthy.length) {
    push('');
    push('### 200 OK with HTML warnings (first 30)');
    for (const r of unhealthy.slice(0, 30)) {
      push(`- ${r.url.replace(SITE, '')} — ${r.issues.join(', ')}`);
    }
    if (unhealthy.length > 30) push(`- … and ${unhealthy.length - 30} more`);
  }
  push('');

  // --- Sister sites ---
  push('## 6. Sister / network sites');
  const sisterResults = await mapPool(SISTER_SITES, async s => ({ ...s, ...(await fetchUrl(s.url)) }), 6);
  for (const s of sisterResults) {
    push(`- **${s.name}** (${s.url}) — HTTP **${s.status || 'ERR'}**${s.error ? ` · ${s.error}` : ''}`);
  }
  push('');

  // --- Internal link spot check on homepage ---
  push('## 7. Homepage internal link spot-check');
  const home = keyResults.find(r => r.url === SITE + '/');
  if (home && home.body) {
    const hrefs = [...home.body.matchAll(/href="(\/[^"#?]+)"/g)].map(m => m[1]);
    const unique = [...new Set(hrefs)]
      .filter(rel => {
        if (!rel.startsWith('//')) return true;
        const host = rel.slice(2).split('/')[0].toLowerCase();
        return !(host.includes('google') || host.includes('googletagmanager') || host.includes('cloudflareinsights'));
      })
      .slice(0, 25);
    const linkChecks = await mapPool(unique, async rel => {
      const r = await fetchUrl(SITE + rel);
      return { rel, status: r.status, ok: r.ok };
    }, CONCURRENCY);
    const dead = linkChecks.filter(l => !l.ok);
    push(`- Checked **${linkChecks.length}** homepage internal links`);
    push(`- Dead links: **${dead.length}**`);
    for (const d of dead) push(`  - ${d.status || 'ERR'} ${d.rel}`);
  }
  push('');

  // --- Random sample for manual QA ---
  push('## 8. Random manual QA URLs');
  const okPages = crawlResults.filter(r => r.ok && r.url.includes('/gyms/'));
  const shuffled = okPages.sort(() => Math.random() - 0.5);
  const picks = [];
  const types = new Set();
  for (const r of shuffled) {
    const t = r.url.includes('/guides/') ? 'guide' : r.url.includes('/category/') ? 'cat' : r.url.includes('/area/') ? 'area' : 'gym';
    if (types.has(t) && picks.length >= 5) continue;
    types.add(t);
    picks.push(r.url);
    if (picks.length >= 5) break;
  }
  while (picks.length < 5 && shuffled[picks.length]) picks.push(shuffled[picks.length].url);
  for (const u of picks.slice(0, 5)) push(`- ${u}`);
  push('');

  // --- Summary ---
  const localPass = localChecks.every(c => c.ok);
  const livePass = keyFails === 0 && broken.length === 0;
  push('## 9. Executive summary');
  push(`| Check | Result |`);
  push(`|-------|--------|`);
  push(`| Local gates | ${localPass ? '✅ PASS' : '❌ FAIL'} |`);
  push(`| Key live pages | ${keyFails === 0 ? '✅ PASS' : `❌ ${keyFails} issue(s)`} |`);
  push(`| Full sitemap | ${broken.length === 0 ? '✅ all 200' : `❌ ${broken.length} broken`} |`);
  push(`| HTML warnings (200) | ${unhealthy.length === 0 ? '✅ none' : `⚠ ${unhealthy.length}`} |`);
  push(`| Sister sites | ${sisterResults.every(s => s.ok) ? '✅ all up' : '⚠ check above'} |`);
  push('');
  push('---');
  push('*Re-run after deploy: `node scripts/full-site-audit.js`*');

  fs.writeFileSync(OUT, lines.join('\n'), 'utf8');
  console.log(`\nWrote ${OUT}`);
  process.exit(localPass && livePass && broken.length === 0 ? 0 : 1);
}

main().catch(e => {
  console.error(e);
  process.exit(1);
});
